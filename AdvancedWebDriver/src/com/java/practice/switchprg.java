package com.java.practice;

public class switchprg {

	public static void main(String[] args) {
		int num = 40;
		switch(num){
		case 10 : System.out.println("number in 10,20,30");
		break;
		case 20: System.out.println("number in 10,20,30");
		break;
		case 30 : System.out.println("number in 10,2,30");
		break;
		default: System.out.println("number not in 10,20,30");
		break;
			
		}
	}

}
