package SeleniumLab;

public class SwitchLab {

	public static void main(String[] args) {
		char grade = 'B';
		switch (grade)
		{
		case 'A':System.out.println("Excellent");
		break;
		case 'B':System.out.println("not excellent");
		break;
		default :System.out.println("invalid grade");
		}
		 
		
	}

}
