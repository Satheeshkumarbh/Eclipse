package JavaLearning;

public class ifelse {
	
	
	
	public static void main(String args[]) {
		int number = 0;
		if(number%2 == 0 )
		{
			System.out.println("even number");
		}
		else if(number%2 != 0)
		{
			System.out.println("odd number");
		}
		else
			System.out.println("zero");
	}
	

}
